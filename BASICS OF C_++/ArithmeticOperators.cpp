#include<iostream>
using namespace std;
int main(){
    //increment and decrement
    int x = 5;
    cout<< x << endl;
    ++x; //pre increment x = x + 1
    cout<< x << endl;
    x++ ; // post increment 
    cout<< x << endl;
    cout<< ++x << endl;
    cout<< x << endl;

    int y = 10;
    cout<< ++y << endl;
    cout<< y << endl;
    y--;     //y = y - 1 post decrement
    cout<< y << endl;
    --y;    //y = y - 1 pre decrement
    cout<< y ;
}
